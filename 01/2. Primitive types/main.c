#include <stdio.h>

int main(void)
{
    int integer_1 = 10;
    long integer_2 = 100;
    float decimal = 1.5f;
    char letter = 'A';

    printf("Int Type Value: %d\n", integer_1);
    printf("Long Type Value: %ld\n", integer_2);
    printf("Float Type Value: %f\n", decimal);
    printf("Char Type Value: %c\n", letter);

	return 0;
}
