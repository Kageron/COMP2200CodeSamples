#include <stdio.h>

int main()
{
	int integer1 = 10;
	long integer2 = 100;
	float decimal = 1.5f;
	char letter = 'A';

	printf("int type value: %d\n", integer1);
	printf("long type value: %l\n", integer2);
	printf("float type value: %f\n", decimal);
	printf("char type value: %c\n", letter);

	return 0;
}