#include <stdio.h>

int main(void)
{
    int integer_1 = 10;
    long integer_2 = 100;
    float decimal = 1.5f;
    char letter = 'A';

    printf("integer_1: %d\n", integer_1);
    printf("integer_2: %ld\n", integer_2);
    printf("decimal: %f\n", decimal);
    printf("letter: %c\n", letter);

    return 0;
}
